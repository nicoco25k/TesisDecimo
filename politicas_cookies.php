    <h1 class="mb-4 pt-5">Política de Tratamiento de Datos Personales</h1>

    <h2>Introducción</h2>
    <p>Este documento tiene la finalidad de establecer la política de tratamiento de datos personales para la plataforma ASOPETSSOFT, la cual se encarga de las adopciones y apadrinamiento de animales en la asociación ASOPATICAS del municipio del Espinal – Tolima. Lo que se busca con esta política es aclararle al usuario, adoptante, padrino y todas las personas interesadas en el cuidado y bienestar animal, el propósito y el uso de sus datos e información personal, como se establece en la ley 1581 de 2012 y sus decretos. Esta ley se encarga de la protección de los datos personales recolectados en bases de datos.</p>
    <p>Es de aclarar que la información recogida a través de la plataforma ASOPETSSOFT solo será utilizada por la asociación ASOPATICAS y podrá ser actualizada en cualquier momento; al usar esta plataforma usted está aceptando la política de tratamiento de datos personales y confirmando que es el titular de la información brindada.</p>

    <h2>Datos e Información Recolectada</h2>
    <p>Los datos e información recolectados por esta plataforma para todos los usuarios son los siguientes:</p>
    <ul>
        <li><strong>Datos de identificación:</strong> En esta parte se encuentra el nombre completo, el tipo de documento y el número de identificación de la persona.</li>
        <li><strong>Datos de contacto:</strong> En este apartado se encuentra el correo electrónico y el número de celular del titular.</li>
        <li><strong>Datos de interacción:</strong> En este punto es necesario tener un nickname, este un nombre único que debe tener cada usuario para ingresar al sistema y el historial de navegación, en el cual se encuentran la lista de animales que se adoptaron, apadrinados y la evidencia del desprendible de las donaciones realizadas, ya que por medio de esta plataforma no se realizan transacciones financieras.</li>
    </ul>
    <p>Se debe aclarar que en ningún momento esta plataforma recolecta datos sensibles, los cuales son los que afectan directamente la intimidad del titular, algunos son: (orientación étnica, política, datos referentes a la salud, entre otros aspectos).</p>

    <h2>Finalidad del Tratamiento de los Datos Recolectados</h2>
    <p>Los datos e información recolectados anteriormente solo serán usados para la interacción dentro de la plataforma ASOPETSSOFT, los cuales son:</p>
    <ul>
        <li>Gestión de los procesos de adopción y apadrinamiento de mascotas, por lo que por medio de esta plataforma se le pide al usuario toda la información requerida para estos procesos y la decisión final será enviada directamente al correo electrónico del usuario por la encargada de la asociación.</li>
        <li>Historial de navegación de todos los procesos realizados en la plataforma, como lo son las adopciones, apadrinamientos, integración en campañas y donaciones.</li>
        <li>Soporte de las donaciones realizadas, ya que por medio de esta plataforma no se hará ningún tipo de transacción financiera, pero sí se llevará un control de los comprobantes de envío, los cuales estarán disponibles en el apartado específico.</li>
        <li>Comunicación con el adoptante o padrino de la mascota para informarle y asegurar el bienestar de esta luego de haber sido entregado, se realizará a través de WhatsApp, correo electrónico y visitas domiciliarias.</li>
        <li>Envío de información relacionada con la plataforma, así como campañas de sensibilización y esterilización de mascotas a través de WhatsApp y correo electrónico.</li>
        <li>Divulgación de actualizaciones realizadas dentro de la plataforma para mejorar el bienestar del usuario, se informará a través de WhatsApp y correo electrónico.</li>
    </ul>

    <h2>Consentimiento</h2>
    <p>El uso de tratamiento de los datos e información recolectados se realiza bajo la autorización del titular de los datos. Esta política debe ser aceptada marcando una casilla al final del formulario para poder registrarse en nuestra plataforma. Este consentimiento puede ser revocado en cualquier momento o cuando el usuario lo requiera por medio de correo electrónico, cumpliendo con lo establecido en la ley 1581 de 2012.</p>

    <h2>Derechos que tiene el Titular de los Datos</h2>
    <p>Los derechos que tienen los usuarios dentro de la plataforma son los siguientes, tanto para navegación, como para interacción con la misma:</p>
    <ul>
        <li>Acceder a los datos e información dentro de la plataforma.</li>
        <li>Rehusarse a que su información sea usada para otros fines que no son los aceptados.</li>
        <li>Que se eliminen sus datos personales luego de haber revocado, este derecho se puede ejercer a través de correo electrónico o directamente en el punto físico de la asociación ASOPATICAS.</li>
        <li>Actualizar sus datos personales cuando sea necesario, el usuario cuando lo requiera puede ingresar al apartado de actualizar datos cuando haya iniciado sesión. Por otro lado, si requiere actualizar la contraseña, solo debe ingresar el correo electrónico para confirmar su identidad dentro de la plataforma y al mismo tiempo se le envía el paso a paso para realizar esta actualización.</li>
    </ul>

    <h2>Periodo de Almacenamiento</h2>
    <p>Estos datos e información personal recolectados serán usados por nuestra plataforma el tiempo que sea necesario y/o cumpla con el propósito establecido en esta política; en otras palabras, quedará de forma permanente en la plataforma, pero no será utilizada por la asociación ASOPATICAS a menos que el usuario realice algún proceso o se le notifique alguna actualización.</p>

    <h2>Modificaciones de la Política de Tratamiento de Datos</h2>
    <p>Esta política de tratamiento de datos está sujeta a modificaciones para mejorar la calidad y la seguridad de los usuarios, además de recalcar que de cualquier modificación se le informará por medio de WhatsApp, correo electrónico y a través de notificaciones dentro de la plataforma.</p>

    <h2>Medidas de Seguridad</h2>
    <p>Las medidas de seguridad que se implementarán en esta política son las copias de seguridad diarias y el uso de contraseñas seguras. Por lo tanto, al momento de crear la contraseña, esta tiene unas pautas como lo son el número mínimo de palabras, números y caracteres especiales.</p>


            <h2>Datos e Información Recolectada</h2>
            <p>Los datos e información recolectados por esta plataforma para todos los usuarios son los siguientes:</p>
            <ul>
                <li><strong>Datos de identificación:</strong> En esta parte se encuentra el nombre completo, el tipo de documento y el número de identificación de la persona.</li>
                <li><strong>Datos de contacto:</strong> En este apartado se encuentra el correo electrónico y el número de celular del titular.</li>
                <li><strong>Datos de interacción:</strong> En este punto es necesario tener un nickname, este un nombre único que debe tener cada usuario para ingresar al sistema y el historial de navegación, en el cual se encuentran la lista de animales que se adoptaron, apadrinados y la evidencia del desprendible de las donaciones realizadas, ya que por medio de esta plataforma no se realizan transacciones financieras.</li>
            </ul>

                <li>Comunicación con el adoptante o padrino de la mascota para informarle y asegurar el bienestar de esta luego de haber sido entregado, se realizará a través de WhatsApp, correo electrónico y visitas domiciliarias.</li>
                <li>Envío de información relacionada con la plataforma, así como campañas de sensibilización y esterilización de mascotas a través de WhatsApp y correo electrónico.</li>
                <li>Divulgación de actualizaciones realizadas dentro de la plataforma para mejorar el bienestar del usuario, se informará a través de WhatsApp y correo electrónico.</li>
            </ul>


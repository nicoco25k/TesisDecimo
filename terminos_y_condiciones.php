            <h2>Introducción</h2>
            <p>Este documento tiene la finalidad de establecer la política de tratamiento de datos personales para la plataforma ASOPETSSOFT, la cual se encarga de las adopciones y apadrinamiento de animales en la asociación ASOPATICAS del municipio del Espinal – Tolima. Lo que se busca con esta política es aclararle al usuario, adoptante, padrino y todas las personas interesadas en el cuidado y bienestar animal, el propósito y el uso de sus datos e información personal, como se establece en la ley 1581 de 2012 y sus decretos. Esta ley se encarga de la protección de los datos personales recolectados en bases de datos.</p>

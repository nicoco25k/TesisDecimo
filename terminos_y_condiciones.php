                <li>Actualizar sus datos personales cuando sea necesario, el usuario cuando lo requiera puede ingresar al apartado de actualizar datos cuando haya iniciado sesión. Por otro lado, si requiere actualizar la contraseña, solo debe ingresar el correo electrónico para confirmar su identidad dentro de la plataforma y al mismo tiempo se le envía el paso a paso para realizar esta actualización.</li>
